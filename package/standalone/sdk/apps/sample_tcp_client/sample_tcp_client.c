static int recv_from_timeout(int sockfd, long sec, long usec)
{
	/* Setup timeval variable */
	struct timeval timeout;
	timeout.tv_sec = sec;
	timeout.tv_usec = usec;

	/* Setup fd_set structure */
	fd_set fds;
	FD_ZERO(&fds);
	FD_SET(sockfd, &fds);

	/* Return value: -1(error occured), 0(timed out), >0(data ready to be read) */
	return select(sockfd+1 , &fds, NULL, NULL, &timeout);
}

static void tcp_client_task(void *pvParameters)
{
	char rx_buffer[RECV_BUF_SIZE];
	int n = 0;
	int destlen = 0;
	int sockfd;
	struct sockaddr_in dest_addr;
	WIFI_CONFIG *param = pvParameters;
	int count = 0;


	/* Assign test is running */
	param->test_running = 1;

	/* build the destination's Internet address */
	bzero((char *) &dest_addr, sizeof(dest_addr));

	// Filling server information
	dest_addr.sin_family = AF_INET;
	dest_addr.sin_port = htons(REMOTE_PORT);
	dest_addr.sin_addr.s_addr = inet_addr((const char *)param->remote_addr);

	sockfd =  socket(AF_INET, SOCK_STREAM, 0);
	if (sockfd < 0) {
		error_val = -1;
		nrc_usr_print("Fail to create socket\n");
		goto exit;
	}
	nrc_usr_print("Socket created, connecting to %s:%d\n", (const char *)param->remote_addr, REMOTE_PORT);

	int err = connect(sockfd, (struct sockaddr *)&dest_addr, sizeof(dest_addr));
	if (err != 0) {
		error_val = -1;
		nrc_usr_print("Fail to connect\n");
		goto exit;
	}
	nrc_usr_print("Successfully connected\n");

	destlen = sizeof(dest_addr);
	while (1) {
		n = send(sockfd, payload, 1470, 0);
		nrc_usr_print("[%d] Send (%d) \n", sockfd, n);

		if (n < 0) {
			error_val = -1;
			nrc_usr_print("Error occurred during sending\n");
			goto exit;
		}

		_delay_ms(1000);
	}
	error_val = 0;

	_delay_ms(5000);


exit:
	if (sockfd >= 0) {
		nrc_usr_print("Shutting down and close socket\n");
		shutdown(sockfd, SHUT_RDWR);
		close(sockfd);

	}
	param->test_running = 0;
	vTaskDelete(NULL);

}


/******************************************************************************
 * FunctionName : run_sample_tcp_client
 * Description  : sample test for tcp client
 * Parameters   : WIFI_CONFIG
 * Returns      : 0 or -1 (0: success, -1: fail)
 *******************************************************************************/
int  run_sample_tcp_client(WIFI_CONFIG *param)
{
	int network_index = 0;
	int wifi_state = WLAN_STATE_INIT;

	nrc_usr_print("[%s] Sample App for run_sample_tcp_client \n",__func__);

	if (wifi_init(param)!= WIFI_SUCCESS) {
		nrc_usr_print ("[%s] ASSERT! Fail for init\n", __func__);
		return RUN_FAIL;
	}

	/* 1st trial to connect */
	if (wifi_connect(param)!= WIFI_SUCCESS) {
		nrc_usr_print ("[%s] Fail for Wi-Fi connection (results:%d)\n", __func__);
		return RUN_FAIL;
	}

	network_index = nrc_wifi_get_network_index();

	if (nrc_wifi_get_state() != WLAN_STATE_GET_IP) {
		nrc_usr_print("[%s] Fail to connect or get IP !\n",__func__);
		return RUN_FAIL;
	}

	xTaskCreate(tcp_client_task, "tcp_client", 4096, (void*)param, 5, NULL);
	while(param->test_running){
		_delay_ms(1);
	}

	wifi_state = nrc_wifi_get_state();

	if (wifi_state == WLAN_STATE_GET_IP || wifi_state == WLAN_STATE_CONNECTED) {
		nrc_usr_print("[%s] Trying to DISCONNECT... for exit\n",__func__);
		if (nrc_wifi_disconnect(network_index) != WIFI_SUCCESS) {
			nrc_usr_print ("[%s] Fail for Wi-Fi disconnection (results:%d)\n", __func__);
			return RUN_FAIL;
		}
	}

	if (error_val < 0)
		return RUN_FAIL;

	nrc_usr_print("[%s] End of run_sample_tcp_client!! \n",__func__);

	return RUN_SUCCESS;
}


/******************************************************************************
 * FunctionName : user_init
 * Description  : Start Code for User Application, Initialize User function
 * Parameters   : none
 * Returns      : none
 *******************************************************************************/
void user_init(void)
{
	int ret = 0;
	WIFI_CONFIG* param;

	nrc_uart_console_enable();

	param = nrc_mem_malloc(WIFI_CONFIG_SIZE);
	memset(param, 0x0, WIFI_CONFIG_SIZE);

	set_wifi_config(param);
	ret = run_sample_tcp_client(param);
	nrc_usr_print("[%s] test result!! %s \n",__func__, (ret==0) ?  "Success" : "Fail");
	if(param){
		nrc_mem_free(param);
	}
}

